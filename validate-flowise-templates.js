// FunÃ§Ã£o para validar um node do Flowise
function validateNode(node) {
  const errors = [];
  const warnings = [];

  // Verificar estrutura bÃ¡sica
  if (!node.id) {
    errors.push('Node sem ID');
  }

  if (!node.type) {
    errors.push('Node sem tipo');
  } else if (!validFlowiseNodes.includes(node.type)) {
    warnings.push(`Tipo de node desconhecido: ${node.type}`);
  }

  if (!node.position) {
    errors.push('Node sem posiÃ§Ã£o');
  } else {
    if (typeof node.position.x !== 'number') {
      errors.push('PosiÃ§Ã£o X invÃ¡lida');
    }
    if (typeof node.position.y !== 'number') {
      errors.push('PosiÃ§Ã£o Y invÃ¡lida');
    }
  }

  if (!node.data) {
    warnings.push('Node sem dados');
  }

  return { errors, warnings };
}

// FunÃ§Ã£o para validar um edge do Flowise
function validateEdge(edge) {
  const errors = [];
  const warnings = [];

  if (!edge.source) {
    errors.push('Edge sem source');
  }

  if (!edge.target) {
    errors.push('Edge sem target');
  }

  return { errors, warnings };
}

// FunÃ§Ã£o para validar a estrutura do workflow
function validateWorkflowStructure(workflow) {
  console.log(colors.yellow + `ðŸ“‹ Validando workflow: ${workflow.name}` + colors.reset);
  
  const config = workflow.config;
  const allErrors = [];
  const allWarnings = [];

  // Validar nodes
  if (config.nodes && config.nodes.length > 0) {
    console.log(colors.blue + `   Nodes: ${config.nodes.length}` + colors.reset);
    
    config.nodes.forEach((node, index) => {
      const { errors, warnings } = validateNode(node);
      
      if (errors.length > 0) {
        console.log(colors.red + `   âŒ Node ${index + 1} (${node.id}): ${errors.join(', ')}` + colors.reset);
        allErrors.push(...errors.map(e => `Node ${node.id}: ${e}`));
      }
      
      if (warnings.length > 0) {
        console.log(colors.yellow + `   âš ï¸  Node ${index + 1} (${node.id}): ${warnings.join(', ')}` + colors.reset);
        allWarnings.push(...warnings.map(w => `Node ${node.id}: ${w}`));
      }
    });
  } else {
    allErrors.push('Workflow sem nodes');
    console.log(colors.red + '   âŒ Workflow sem nodes' + colors.reset);
  }

  // Validar edges
  if (config.edges && config.edges.length > 0) {
    console.log(colors.blue + `   Edges: ${config.edges.length}` + colors.reset);
    
    config.edges.forEach((edge, index) => {
      const { errors, warnings } = validateEdge(edge);
      
      if (errors.length > 0) {
        console.log(colors.red + `   âŒ Edge ${index + 1}: ${errors.join(', ')}` + colors.reset);
        allErrors.push(...errors.map(e => `Edge ${index}: ${e}`));
      }
      
      if (warnings.length > 0) {
        console.log(colors.yellow + `   âš ï¸  Edge ${index + 1}: ${warnings.join(', ')}` + colors.reset);
        allWarnings.push(...warnings.map(w => `Edge ${index}: ${w}`));
      }
    });
  } else {
    allWarnings.push('Workflow sem edges');
    console.log(colors.yellow + '   âš ï¸  Workflow sem edges' + colors.reset);
  }

  // Validar conectividade
  if (config.nodes && config.edges) {
    const nodeIds = new Set(config.nodes.map(n => n.id));
    const disconnectedNodes = [];
    
    config.nodes.forEach(node => {
      const hasIncomingEdge = config.edges.some(e => e.target === node.id);
      const hasOutgoingEdge = config.edges.some(e => e.source === node.id);
      
      if (!hasIncomingEdge && !hasOutgoingEdge) {
        disconnectedNodes.push(node.id);
      }
    });
    
    if (disconnectedNodes.length > 0) {
      allWarnings.push(`Nodes desconectados: ${disconnectedNodes.join(', ')}`);
      console.log(colors.yellow + `   âš ï¸  Nodes desconectados: ${disconnectedNodes.join(', ')}` + colors.reset);
    }
  }

  return {
    isValid: allErrors.length === 0,
    errors: allErrors,
    warnings: allWarnings
  };
}

